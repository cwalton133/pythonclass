## For Loops 

for letter in "Giraffee Academy":
    print(letter)
    

friends = ["Ade", "Mike", "John", "Jim", "Joshua"]
for friend in friends:
    print(friend)

for index in range(len(friends)):
    print(friends[index])
    
for index in range(10):
    print(index)
    
for index in range(3, 10):
    print(index)
    
    
