x = "Hello World"	## str	data type
print(x)
print(type(x))
x = 20	## int data type
print(x)
x = 20.5	## float data type	
print(x)
x = 1j	## complex data type 
print(x)
x = ["apple", "banana", "cherry"]	## list	data type
print(x)
x = ("apple", "banana", "cherry")	## tuple data type
print(x)
x = range(6)	## range data type
print(x)
x = {"name" : "John", "age" : 36}	## dict	data type
print(x)
x = {"apple", "banana", "cherry"}	## set data type
print(x)
x = frozenset({"apple", "banana", "cherry"})	## frozenset data type
print(x)
x = True	## bool	data type
print(x)
x = b"Hello"	## bytes data type	
print(x)
x = bytearray(5)	## bytearray data type	
print(x)
x = memoryview(bytes(5))	## memoryview data type
print(type(x))
